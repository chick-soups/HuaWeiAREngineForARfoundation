 HWAR_TRACKABLE_BASE_TRACKABLE

基本可追踪类型，取值为0x41520100。

HWAR_TRACKABLE_PLANE

平面追踪类型，取值为0x41520101。

HWAR_TRACKABLE_POINT

点追踪类型，取值为0x41520102。

HWAR_TRACKABLE_AUGMENTED_IMAGE

图像追踪类型，取值为0x41520104。

HWAR_TRACKABLE_HAND

手追踪类型，取值为0x50000000。

HWAR_TRACKABLE_BODY

身体追踪类型，取值为0x50000001。

HWAR_TRACKABLE_FACE

脸部追踪类型，取值为0x50000002。

HWAR_TRACKABLE_TARGET_DETECTION

目标检测，取值为0x50000003。

HWAR_TRACKABLE_NOT_VALID

无效追踪，取值为0。
*/